package task2and3;

public class Student {
    private String name;
    private int year;

    public void setName(String newName) {
        this.name = newName;
    }

    public String getName(){
        return this.name;
    }


    public void setYear(int newYear) {
        this.year = newYear;
    }

    public int getYear(){
        return this.year;
    }

}